    private void buttonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonBuscarActionPerformed
        DefaultTableModel modeloTableClientes = (DefaultTableModel) tabelaCliente.getModel();
        modeloTableClientes.setNumRows(0);
        
        for(Cliente c : clienteController.getListaClientesporNome(textFieldBuscar.getText())){
            modeloTableClientes.addRow(new Object[]{
            c.getIdCliente(),
            c.getNome().trim(),
            c.getEmail(),
            c.getCpf(),
            c.getTelefone(),
            c.getDataAniversario()});
        }
    }//GEN-LAST:event_buttonBuscarActionPerformed

    private void buttonAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAtualizarActionPerformed
         if(tabelaCliente.getSelectedRow() != -1){
            clienteController.update((int) tabelaCliente.getValueAt(tabelaCliente.getSelectedRow(), 0), 
                    textFieldNome.getText(), 
                    textFieldEmail.getText(),
                    textFieldCPF.getText(),
                    textFieldTelefone.getText(),
                    textFieldDataAniversario.getText());
            
            this.getListaClientes();
            limpaCampos();
        }
    }//GEN-LAST:event_buttonAtualizarActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        this.getListaClientes();
    }//GEN-LAST:event_formWindowOpened

    private void tabelaClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaClienteMouseClicked
       if(tabelaCliente.getSelectedRow() != -1){
           textFieldNome.setText(tabelaCliente.getValueAt(tabelaCliente.getSelectedRow(), 1).toString());
            textFieldBuscar.setText(tabelaCliente.getValueAt(tabelaCliente.getSelectedRow(), 1).toString());
            textFieldEmail.setText(tabelaCliente.getValueAt(tabelaCliente.getSelectedRow(),2).toString());
            textFieldCPF.setText(tabelaCliente.getValueAt(tabelaCliente.getSelectedRow(),3).toString());
            textFieldTelefone.setText(tabelaCliente.getValueAt(tabelaCliente.getSelectedRow(),4).toString());
            textFieldDataAniversario.setText(tabelaCliente.getValueAt(tabelaCliente.getSelectedRow(),5).toString());
        }
    }//GEN-LAST:event_tabelaClienteMouseClicked

    private void buttonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonExcluirActionPerformed
        if(tabelaCliente.getSelectedRow() != -1){
            clienteController.delete((int) tabelaCliente.getValueAt(tabelaCliente.getSelectedRow(),0));
            
            this.getListaClientes(); 
            limpaCampos();
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um cliente para excluir!");
        }
    }//GEN-LAST:event_buttonExcluirActionPerformed

