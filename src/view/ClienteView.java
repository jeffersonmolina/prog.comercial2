    public void limpaCampos(){
        textFieldNome.setText("");
        textFieldEmail.setText("");
        textFieldCPF.setText("");
        textFieldTelefone.setText("");
        textFieldDataAniversario.setText("");
        textFieldNome.requestFocus();
    }
    
    public void getListaClientes(){
        DefaultTableModel modeloTableClientes = (DefaultTableModel) tabelaCliente.getModel();
        modeloTableClientes.setNumRows(0);
        for(Cliente c : clienteController.read()){
            modeloTableClientes.addRow(new Object[]{
            c.getIdCliente(),
            c.getNome().trim(),
            c.getEmail(),
            c.getCpf(),
            c.getTelefone(),
            c.getDataAniversario()});
        }
    }

