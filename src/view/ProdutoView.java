    private void buttonAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAtualizarActionPerformed
        if(tabelaProdutos.getSelectedRow() != -1){
            produtoController.update((int) tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 0), 
                    textFieldDescricao.getText(), 
                    Integer.parseInt(textFieldQuantidade.getText()), 
                    Double.parseDouble(textFieldPreco.getText()));
            
            this.getListaProdutos();
            limpaCampos();
        }
    }//GEN-LAST:event_buttonAtualizarActionPerformed

    private void buttonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCadastrarActionPerformed
        produtoController.create(textFieldDescricao.getText(),
                Integer.parseInt(textFieldQuantidade.getText()),
                Double.parseDouble(textFieldPreco.getText()));
        
        this.getListaProdutos();
        
        limpaCampos();
    }//GEN-LAST:event_buttonCadastrarActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        this.getListaProdutos();
    }//GEN-LAST:event_formWindowOpened

    private void tabelaProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaProdutosMouseClicked
        if(tabelaProdutos.getSelectedRow() != -1){
            textFieldDescricao.setText(tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 1).toString());
            textFieldBuscar.setText(tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 1).toString());
           textFieldQuantidade.setText(tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 2).toString());
           textFieldPreco.setText(tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(), 3).toString());
        }
    }//GEN-LAST:event_tabelaProdutosMouseClicked

    private void buttonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonExcluirActionPerformed
        if(tabelaProdutos.getSelectedRow() != -1){
            produtoController.delete((int) tabelaProdutos.getValueAt(tabelaProdutos.getSelectedRow(),0));
            
            this.getListaProdutos(); 
            limpaCampos();
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um produto para excluir!");
        }
    }//GEN-LAST:event_buttonExcluirActionPerformed

    private void buttonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonBuscarActionPerformed
        DefaultTableModel modeloTableProdutos = (DefaultTableModel) tabelaProdutos.getModel();
        modeloTableProdutos.setNumRows(0);
        
        for(Produto p : produtoController.getListaProdutosporDescricao(textFieldBuscar.getText())){
            modeloTableProdutos.addRow(new Object[]{
            p.getId(),
            p.getDescricao().trim(),
            p.getQtd(),
            p.getPreco()});
        }
    }//GEN-LAST:event_buttonBuscarActionPerformed

