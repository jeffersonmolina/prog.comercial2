    public void limpaCampos(){
        textFieldDescricao.setText("");
        textFieldQuantidade.setText("");
        textFieldPreco.setText("");
        textFieldDescricao.requestFocus();
    }
    
    public void getListaProdutos(){
        DefaultTableModel modeloTableProdutos = (DefaultTableModel) tabelaProdutos.getModel();
        modeloTableProdutos.setNumRows(0);
        
        for(Produto p : produtoController.read()){
            modeloTableProdutos.addRow(new Object[]{
                p.getId(),
                p.getDescricao().trim(),
                p.getQtd(),
                p.getPreco()});
        }
    }

